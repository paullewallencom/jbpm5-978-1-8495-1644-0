    private void refreshTaskList(){
        String selectedUser = (String) selectedUserjComboBox.getSelectedItem();
        System.out.println("Selected User: " + selectedUser);
        List<TaskSummary> tasks = localTaskService.getTasksAssignedAsPotentialOwner(selectedUser, "en-UK");
        System.out.println("Tasks: " + tasks + " - size: " + tasks.size());
        String selectedPerspective = (String) perspectivejComboBox.getItemAt(perspectivejComboBox.getSelectedIndex());
        if (selectedPerspective.equals("Simple")) {
            taskSummariesjTable.setModel(new SimpleTaskSummariesModel(tasks));
        } else {
            taskSummariesjTable.setModel(new DetailedTaskSummariesModel(tasks));
        }
        
    }
    

    private void synchronizeUserComboBoxModel(){
        int cboAssignToIndex = this.cboAssignTo.getSelectedIndex();
        int selectedUserjComboBoxIndex = this.selectedUserjComboBox.getSelectedIndex();
        
        this.userComboBoxModel = new DefaultComboBoxModel();
        for (String user : this.users.keySet()) {
            this.userComboBoxModel.addElement(user);
        }
        
        this.cboAssignTo.setModel(userComboBoxModel);
        this.selectedUserjComboBox.setModel(userComboBoxModel);
        
        this.cboAssignTo.setSelectedIndex(cboAssignToIndex);
        this.selectedUserjComboBox.setSelectedIndex(selectedUserjComboBoxIndex);
    }

    
    /**
     * Creates a Map from the parameters table and marshalls it.
     * @return 
     */
    private ContentData createContentData(){
        
        Map<String, String> parameters = new LinkedHashMap<String, String>();

        int paramColumnIndex = TaskParametersTableModel.ColumnHeaders.PARAMETER.ordinal();
        int valueColumnIndex = TaskParametersTableModel.ColumnHeaders.VALUE.ordinal();
        
        for (int row = 0; row < this.taskParametersTableModel.getRowCount(); row++) {
            String paramName = (String) this.taskParametersTableModel.getValueAt(row, paramColumnIndex);
            String value = (String) this.taskParametersTableModel.getValueAt(row, valueColumnIndex);
            parameters.put(paramName, value);
        }
        
        return ContentMarshallerHelper.marshal(parameters, null);
    }
    
