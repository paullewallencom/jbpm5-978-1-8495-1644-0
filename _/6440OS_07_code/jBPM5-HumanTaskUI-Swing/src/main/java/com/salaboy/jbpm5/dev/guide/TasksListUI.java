    /**
     * Custom task event listener to react upon events generated by 
     * the Task Service.
     */
    private class UITaskEventListener extends DefaultTaskEventListener{

        @Override
        public void taskCompleted(TaskUserEvent event) {
            //when a task is completed we want to close the opened form
            Component taskForm = openedTaskForms.remove(event.getTaskId());
            tasklistsjTabbedPane.remove(taskForm);
            
            //go back to task list
            tasklistsjTabbedPane.setSelectedIndex(0);
            
            //display a message
            JOptionPane.showMessageDialog(TasksListUI.this, "Task "+event.getTaskId()+" was Completed","Task Completed",JOptionPane.INFORMATION_MESSAGE);
        }

        @Override
        public void taskFailed(TaskUserEvent event) {
            //display a message
            JOptionPane.showMessageDialog(TasksListUI.this, "Task "+event.getTaskId()+" Failed!","Task Failed",JOptionPane.WARNING_MESSAGE);
        }
        
        
    }
    
